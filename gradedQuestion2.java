public class gradedQuestion2 {
    public static void main(String[] args) { 
        int num1 = 10;

        num1 += 2;
        System.out.println("After addition: num1 = " + num1);

        num1 -= 4;
        System.out.println("After subtraction: num1 = " + num1);

        num1 *= 7;
        System.out.println("After multiplication: num1 = " + num1);

        num1 /= 4;
        System.out.println("After division: num1 = " + num1);
        
        num1 %= 3;
        System.out.println("After modulus: num1 = " + num1);
    }
       
}